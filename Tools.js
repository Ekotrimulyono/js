var bioinput = document.getElementById("bioinput");
var biooutput = document.getElementById("biooutput");

// Set pointer-events none as default
biooutput.style.pointerEvents = "none";

bioinput.addEventListener(
  "input",
  function() {
    biooutput.value = "";
    var val = this.value;
    val = val.split("");

    if (bioinput.value == "") {
      biooutput.style.pointerEvents = "none";
    } else {
      biooutput.style.pointerEvents = "auto";
    }

    setTimeout(function() {
      Object.keys(ekobio).map(function(item) {
        //Put for loop to convert same letters also
        for (var j = 0; j < val.length; j++) {
          var i = val.indexOf(item);
          if (i !== -1) {
            val[i] = ekobio[item];
            biooutput.value = val.join(" ");

            // Detect line break
            if (val.indexOf("\n") !== -1) {
              biooutput.value = trimIt(val.join(" "));
            }
          }
        }
      });
    }, 50);
  },
  true
);

// To trim first letter's white space in new line
function trimIt(x) {
  return x.replace(/^\s+|\s+$/gm, "");
}
